namespace IniFileNet
{
	/// <summary>
	/// Denotes the character used to write a key/value pair.
	/// </summary>
	public enum KeyDelimStyle
	{
		/// <summary>
		/// The = character, which is the default.
		/// </summary>
		Equals,
		/// <summary>
		/// The : character.
		/// </summary>
		Colon,
	}
}
