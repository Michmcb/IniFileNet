namespace IniFileNet
{
	/// <summary>
	/// Denotes the character used to write a comment.
	/// </summary>
	public enum CommentStyle
	{
		/// <summary>
		/// The ; character, which is the default.
		/// </summary>
		Semicolon,
		/// <summary>
		/// The # character.
		/// </summary>
		NumberSign,
	}
}
